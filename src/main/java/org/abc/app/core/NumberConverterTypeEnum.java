package org.abc.app.core;

/**
 * Enum representing different types of number conversions.
 */
public enum NumberConverterTypeEnum {
    /**
     * Conversion from decimal to Roman numerals.
     */
    DECIMAL_TO_ROMAN,

    /**
     * Conversion from binary to Roman numerals.
     */
    BINARY_TO_ROMAN,

    /**
     * Conversion from binary to decimal.
     */
    BINARY_TO_DECIMAL
}
